export const skillRegex: RegExp = new RegExp(
  Object.entries(skillKeywords)
    .flatMap(([key, skill]) => [skill.name, ...(skill.aliases || [])])
    .map((name) => `\\b${name.replace(/[-/\\^$*+?.()|[\]{}]/g, "\\$&")}\\b`)
    .join("|"),
  "gi"
);

export const skillAliasMap: Record<string, string> = Object.entries(
  skillKeywords
).reduce((acc, [key, skill]) => {
  acc[skill.name.toLowerCase()] = skill.name;
  (skill.aliases || []).forEach((alias) => {
    acc[alias.toLowerCase()] = skill.name;
  });
  return acc;
}, {} as Record<string, string>);
